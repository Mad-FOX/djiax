package com.misternerd.djiax.exception;

public class InvalidArgumentException extends IaxException
{

	private static final long serialVersionUID = 1L;


	public InvalidArgumentException()
	{
		super();
	}


	public InvalidArgumentException(String message)
	{
		super();
	}


	public InvalidArgumentException(Throwable tr)
	{
		super(tr);
	}


	public InvalidArgumentException(String Message, Throwable tr)
	{
		super(tr);
	}

}
